            <span class="navbar-text">
                Universitas Sumatera Utara (USU) adalah salah satu perguruan tinggi negeri terbaik di Indonesia yang
                berlokasi di Medan, Sumatera Utara. Awalnya berdiri pada tahun 1952 dengan nama Yayasan Universitet
                Sumatera Utara, USU telah berkembang menjadi universitas unggulan yang menawarkan berbagai program studi
                yang berkualitas. Presiden Soekarno meresmikan USU sebagai universitas negeri ke-7 di Indonesia pada
                tahun 1957. Pada tahun 2003, USU naik status menjadi Perguruan Tinggi Negeri Badan Hukum (PTN-BH) dan
                menjadi universitas PTN-BH ke-5 di Indonesia. Berbagai keunggulan yang dimiliki, seperti fasilitas
                modern dan tenaga ajar yang ahli, menjadikannya sebagai salah satu universitas terbaik di Indonesia.
